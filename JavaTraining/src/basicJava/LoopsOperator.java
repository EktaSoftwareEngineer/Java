package basicJava;

public class LoopsOperator {

	public static void main(String[] args) {
		
		// while loop 
	int i =1;
	while(i<10) {
		System.out.println(i);
		i++;
	}
		//for loop 
	System.out.println("this is for loop ");
	for(int j=0;j<10;j++) {
		System.out.println(j);
	}
		

	}

}
